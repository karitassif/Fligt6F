public class Passenger {
    private String kennitala;
    private String name;

    public Passenger(String kennitala, String name){
        this.kennitala = kennitala;
        this.name = name;
    }

    public String getKennitala() {
        return kennitala;
    }

    public String getName() {
        return name;
    }
}
